USE `pindou_timer`;

-- =============================================
-- 添加父订单ID字段
-- =============================================
ALTER TABLE `biz_order`
ADD COLUMN `parent_order_id` VARCHAR(36) DEFAULT NULL COMMENT '父订单ID（NULL表示为父订单/独立订单）' AFTER `id`;

-- =============================================
-- 添加索引以优化查询性能
-- =============================================
ALTER TABLE `biz_order`
ADD INDEX `idx_parent_order_id` (`parent_order_id`);

